"""
题目描述
计算 1- 1/2 + 1/3 - 1/4 +1/5 -1/6 + ... 的值，直到最后一项的绝对值不大于从键盘输入的某一个正值e。输出计算的结果，保留4位小数。
输入
0.1
输出
0.6456
样例输入 复制
0.01
样例输出 复制
0.6882
"""

e = float(input())

total = 0.0
flag = 1
for i in range(1,1000000000):
    total += (1/i*flag)
    flag *= -1
    if 1 / i <= e:
        print(f"{total:.4f}")
        break
